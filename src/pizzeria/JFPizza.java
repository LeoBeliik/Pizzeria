    private void loadEntities() {
        DefaultTableModel dt = new DefaultTableModel();
        String[] col = new String[6];
        col[0] = "Id";
        dt.setColumnIdentifiers(col);
        col[1] = "Nombre";
        dt.setColumnIdentifiers(col);
        col[2] = "Precio";
        dt.setColumnIdentifiers(col);
        col[3] = "Tipo";
        dt.setColumnIdentifiers(col);
        col[4] = "Variedad";
        dt.setColumnIdentifiers(col);
        col[5] = "Tamaño";
        dt.setColumnIdentifiers(col);

        List<Pizzas> result = entityService.getAll();

        for (Pizzas c : result) {
            String[] row = new String[6];
            row[0] = String.valueOf(c.getId());
            row[1] = c.getNombre();
            row[2] = String.valueOf(c.getPrecio());
            row[3] = c.getTipopizza().getNombre();
            row[4] = c.getVariedadpizza().getNombre();
            row[5] = c.getTamaniopizza().getNombre();
            dt.addRow(row);
        }
        this.jTable1.setModel(dt);
        jTable1.removeColumn(jTable1.getColumnModel().getColumn(0));

    }

    private void loadCombos() {

        cmbTipo.removeAllItems();
        cmbVariedad.removeAllItems();
        cmbTamanio.removeAllItems();

        tipo = entityService.getListTipoPizzas();
        for (TipoPizzas t : tipo) {
            cmbTipo.addItem(t.getNombre());
        }

        variedad = entityService.getListVariedadPizzas();
        for (VariedadPizzas t : variedad) {
            cmbVariedad.addItem(t.getNombre());
        }

        tamanio = entityService.getListTamanioPizzas();
        for (TamanioPizzas t : tamanio) {
            cmbTamanio.addItem(t.getNombre());
        }
    }

