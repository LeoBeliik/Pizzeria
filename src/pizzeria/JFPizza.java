    private void btnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarMouseClicked

        if (txtNombre.getText().equals("")) {
            return;
        }

        if (txtPrecio.getText().equals("")) {
            return;
        }

        if (idPizza.getText().equals("0")) {

            entity = new Pizzas();

        } else {

            entity = entityService.find(Integer.parseInt(idPizza.getText()));

        }

        try {

            entity.setNombre(txtNombre.getText());
            entity.setPrecio(Double.parseDouble(txtPrecio.getText()));

            entity.setTipopizza(tipo.get(cmbTipo.getSelectedIndex()));
            entity.setVariedadpizza(variedad.get(cmbVariedad.getSelectedIndex()));
            entity.setTamaniopizza(tamanio.get(cmbTamanio.getSelectedIndex()));

            entityService.save(entity);

        } catch (NumberFormatException e) {

            JOptionPane.showMessageDialog(txtPrecio, "El precio no puede ser texto o estar en blanco");
            return;

        }

        txtNombre.setText("");
        txtPrecio.setText("");
        idPizza.setText("0");

        txtNombre.setEditable(false);
        txtPrecio.setEditable(false);
        cmbTipo.setEditable(false);
        cmbVariedad.setEditable(false);
        cmbTamanio.setEditable(false);

        loadEntities();

    }//GEN-LAST:event_btnGuardarMouseClicked

