    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        Integer row = jTable1.getSelectedRow();
        idPizza.setText((String) jTable1.getModel().getValueAt(row, 0));
        entity = entityService.find(Integer.parseInt(idPizza.getText()));
        txtNombre.setText(entity.getNombre());
        txtPrecio.setText(String.valueOf(entity.getPrecio()));
        
        for (int i = 1; i <= tipo.size(); i++) {
            if (tipo.get(i - 1).getId() == entity.getTipopizza().getId()) {
                cmbTipo.setSelectedIndex(i - 1);
                break;
            }

        }

        for (int i = 1; i <= variedad.size(); i++) {
            if (variedad.get(i - 1).getId() == entity.getVariedadpizza().getId()) {
                cmbVariedad.setSelectedIndex(i - 1);
                break;
            }

        }

        for (int i = 1; i <= tamanio.size(); i++) {
            if (tamanio.get(i - 1).getId() == entity.getTamaniopizza().getId()) {
                cmbTamanio.setSelectedIndex(i - 1);
                break;
            }

        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jTable1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable1KeyPressed
        if (evt.getKeyCode() == 127) {

            JDialog.setDefaultLookAndFeelDecorated(true);
            int response = JOptionPane.showConfirmDialog(null, "Seguro desea eliminar el registro? ", "Confirmar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (response == JOptionPane.YES_OPTION) {

                Integer row = jTable1.getSelectedRow();
                idPizza.setText((String) jTable1.getModel().getValueAt(row, 0));
                entity = entityService.find(Integer.parseInt(idPizza.getText()));
                entityService.delete(entity);
                loadEntities();

            }

        }
    }//GEN-LAST:event_jTable1KeyPressed

