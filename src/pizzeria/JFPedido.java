    private void loadEntities() {

        DefaultTableModel dt = new DefaultTableModel();
        String[] col = new String[7];
        col[0] = "Pizza";
        dt.setColumnIdentifiers(col);
        col[1] = "Cantidad";
        dt.setColumnIdentifiers(col);
        col[2] = "Precio";
        dt.setColumnIdentifiers(col);
        col[3] = "Total";
        dt.setColumnIdentifiers(col);
        col[4] = "Estado";
        dt.setColumnIdentifiers(col);
        col[5] = "ID";
        dt.setColumnIdentifiers(col);
        col[6] = "IDPedido";
        dt.setColumnIdentifiers(col);

        List<DetallePedidos> result = entity.getDetalle();

        for (DetallePedidos c : result) {

            String[] row = new String[7];
            row[0] = c.getPizza().getNombre();
            row[1] = String.valueOf(c.getCantidad());
            row[2] = String.valueOf(c.getPrecio());
            row[3] = String.valueOf(c.getTotal());
            row[4] = c.getEstadopedidos().getNombre();
            row[5] = String.valueOf(c.getId());
            row[6] = String.valueOf(c.getPedidos().getIdPedidos());
            dt.addRow(row);

        }

        this.jTable1.setModel(dt);

        //jTable1.removeColumn(jTable1.getColumnModel().getColumn(6));
        //jTable1.removeColumn(jTable1.getColumnModel().getColumn(5));
    }

    private void loadCombos() {

        cmbEstado.removeAllItems();

        estado = entityService.getListEstadoPedidos();
        for (EstadoPedidos t : estado) {
            cmbEstado.addItem(t.getNombre());
        }

        cmbPizza.removeAllItems();

        pizzas = entityService.getListPizzas();
        for (Pizzas t : pizzas) {
            cmbPizza.addItem(t.getNombre());
        }
    }

