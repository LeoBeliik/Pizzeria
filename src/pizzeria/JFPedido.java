    private void btnBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarMouseClicked

        btnCancel.setEnabled(false);
        JDBuscarFactura c = new JDBuscarFactura(this, true);
        c.setVisible(true);

        if (c.idPed > 0) {

            entity = entityService.find(c.idPed);

            SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");

            txtCreacion.setText(formatter.format(entity.getFechaHoraCreacion()));
            txtEntrega.setText(formatter.format(entity.getFechaHoraEntrega()));

            txtCliente.setText(entity.getNombreCliente());
            txtNumero.setText(String.valueOf(entity.getNumeroPedidos()));

            for (int i = 1; i <= estado.size(); i++) {
                if (estado.get(i - 1).getIdEstadoPedidos() == entity.getEstadopedidos().getIdEstadoPedidos()) {
                    cmbEstado.setSelectedIndex(i - 1);
                    break;
                }

            }

            loadEntities();

        }
    }//GEN-LAST:event_btnBuscarMouseClicked

