    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked


        try {

            SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");

            entity.setFechaHoraCreacion(formatter.parse(txtCreacion.getText()));
            entity.setFechaHoraEntrega(formatter.parse(txtEntrega.getText()));

            entity.setNombreCliente(txtCliente.getText());
            entity.setEstadopedidos(estado.get(cmbEstado.getSelectedIndex()));
            entity.setNumeroPedidos(Integer.valueOf(txtNumero.getText()));

            DetallePedidos linea = new DetallePedidos();

            linea.setPizza(pizzas.get(cmbPizza.getSelectedIndex()));
            linea.setCantidad(Integer.parseInt(txtCant.getText()));
            linea.setPrecio(Double.parseDouble(txtPrecio.getText()));
            linea.setTotal(linea.getPrecio() * linea.getCantidad());
            linea.setEstadopedidos(estado.get(cmbEstado.getSelectedIndex()));
            linea.setPedidos(entity);

            entity.getDetalle().add(linea);
            entityService.save(entity);
            loadEntities();

            txtPrecio.setText("");
            txtCant.setText("");
            btnCancel.setEnabled(true);

        } catch (NumberFormatException | ParseException e) {
            JOptionPane.showMessageDialog(txtNumero, e);

        }
    }//GEN-LAST:event_btnAddMouseClicked

